function plot_results(S,Theta,C,err_S,err_Theta)
    figure('units','normalized','outerposition',[0 0 1 1])
    subplot(2,4,1)
    imagesc(C)
    title('C')
    colorbar()
    subplot(2,4,5)
    plot(eig(C),LineWidth=2)
    title('C eigs')
    grid on
    subplot(2,4,2)
    imagesc(S)
    title(['S hat, err:' num2str(err_S)])
    colorbar()
    subplot(2,4,6)
    plot(eig(S),LineWidth=2)
    title('S hat eigs')
    grid on 
    subplot(2,4,3)
    imagesc(Theta-diag(diag(Theta)))
    title(['Theta-diag hat, err: ' num2str(err_Theta)])
    colorbar()
    subplot(2,4,7)
    plot(eig(Theta-diag(diag(Theta))),LineWidth=2)
    title('Theta-diag hat eigs')
    grid on
    subplot(2,4,4)
    imagesc(Theta)
    title('Theta hat')
    colorbar()
    subplot(2,4,8)
    plot(eig(Theta),LineWidth=2)
    title('Theta hat eigs')    
    grid on
end